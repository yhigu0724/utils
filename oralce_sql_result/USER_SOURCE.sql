# RHELのプロンプトで実行
sqlplus -S NISHI/iku3952@sjisdb << 'EOF'
SET PAGESIZE 0
SET LINESIZE 32767
SET FEEDBACK OFF
SET TRIMSPOOL ON
SET TERMOUT OFF
SET ECHO OFF
SET LONG 2000000

-- 全オブジェクトを個別のファイルに出力するスクリプトを生成
SPOOL run_extract.sql
SELECT 
    'SPOOL source_sql/' || OBJECT_NAME || '.sql' || CHR(10) ||
    'SELECT TEXT FROM USER_SOURCE WHERE NAME = ''' || OBJECT_NAME || ''' AND TYPE = ''' || OBJECT_TYPE || ''' ORDER BY LINE;' || CHR(10) ||
    'SELECT ''/'' FROM DUAL;' || CHR(10) ||
    'SPOOL OFF'
FROM USER_OBJECTS 
WHERE OBJECT_TYPE IN ('PROCEDURE', 'FUNCTION', 'PACKAGE', 'PACKAGE BODY', 'TRIGGER');
SPOOL OFF

-- 抽出実行
@run_extract.sql
EXIT
EOF
